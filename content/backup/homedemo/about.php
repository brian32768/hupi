    <div id="infotitle" class="hardwire">About Human Power Institute<div id="infocredit">Brian Wilson | 07:03 16.Jun.2004</div></div>
    <div id="infocontent">
     Launched in January, 2004, Human Power Institute (HuPI) seeks to establish a website information database and foster the international exchange of information among all parties interested in the technologies and benefits of human power.<br /><br />
     HuPI is to be a locus for research and development of human power in a scientific and engineering context. Additionally, HuPI is devoted to exploring and understanding how human power technology benefits society as a whole.<br /><br />
     HuPI´s first project was initiated and sponsored by Dave Wilson, editor of Human Power for 18 years. He wished to make the wealth of information in previous issues of Human Power more easily accessible, and to this end commissioned a compilation of all issues in PDF format.<br /><br />
     In June 2004, HuPI started the Human Power eJournal (HPeJ), a web-based open access publication. Initially editted by Theo Schmidt, HPeJ is available via this website.<br /><br />
     Founders of HuPI are: Richard Ballantine, Theo Schmidt, John Snyder, Elrey, Brian Wilson, and David Gordon Wilson.
